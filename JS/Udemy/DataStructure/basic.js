/** Data Structure|| Estrutura de Dados
 * 
 * São formas de organizar, armazenar e manipular dados na memória do computador de maneira eficiente.
 * 
 * Linear || Lineares:
 * Array - Coleção ordenada de elmnts.
 * Stack(Pilha) - LIFO(Last In, First Out). 
 * Queue(Fila) - FIFO(First In, First Out).
 * Deque(Fila Dupla) - Mistura de Stack e Queue.
 * 
 * Non-Liniar || Não Linieares:
 * Linked List(Listas Ligadas) - Sequência encadeadas de nós.
 * Graphs(Grafos) - Representação de conexões entre elmnts.
 * Trees(Árvores) - Estruturas híerarquicas, como Árvore Binária de Busca.
 * 
 * Associative Structure || Estruturas Associativas:
 * Objects(Objetos) - Estrutura Chave-valor.
 * Map & Set - Alternativas otimizadas para armazenar dados únicos.
 * 
 * Hashing:
 * Hash Tables - Funções de espalhamento para buscas rápidas.
 * 
 */